ACTIVITY_FACTORS = {
    "sedentary": 1.2,
    "light": 1.375,
    "moderate": 1.55,
    "active": 1.725,
    "very_active": 1.9
}


def calculate_bmi(weight_kg, height_cm):
    height_m = height_cm / 100.0
    return round(weight_kg / (height_m ** 2), 1)


def bmi_category(bmi):
    if bmi < 18.5:
        return "underweight"
    if bmi < 25:
        return "normal weight"
    if bmi < 30:
        return "overweight"
    if bmi < 35:
        return "class 1 obesity"
    if bmi < 40:
        return "class 2 obesity"
    return "class 3 obesity"


def calculate_bmr(weight_kg, height_cm, age, gender):
    """
    Mifflin-St Jeor equation.
    gender should be: male, female, or unknown.
    """
    base = 10 * weight_kg + 6.25 * height_cm - 5 * age

    if gender == "male":
        return round(base + 5)

    if gender == "female":
        return round(base - 161)

    return round(base - 78)


def calculate_tdee(bmr, activity_level):
    factor = ACTIVITY_FACTORS.get(activity_level, 1.2)
    return round(bmr * factor)


def calculate_calorie_target(age, bmr, tdee, goal):
    """
    Safety rule:
    For users under 18, the assistant does not recommend a calorie deficit.
    """
    notes = []

    if age < 18:
        notes.append(
            "For users under 18, aggressive calorie restriction, fasting, strict dieting, or meal skipping is not recommended. The focus should be healthy growth, balanced meals, adequate sleep, and safe activity."
        )
        return {
            "target_calories": tdee,
            "strategy": "maintenance_for_growth",
            "notes": notes
        }

    if goal == "lose_weight":
        target = max(bmr, tdee - 350)
        notes.append(
            "A mild calorie deficit was selected, and the target was not set below estimated BMR."
        )
        return {
            "target_calories": round(target),
            "strategy": "mild_deficit",
            "notes": notes
        }

    if goal == "gain_weight":
        return {
            "target_calories": round(tdee + 300),
            "strategy": "mild_surplus",
            "notes": [
                "A mild calorie surplus was selected for gradual weight gain."
            ]
        }

    return {
        "target_calories": tdee,
        "strategy": "maintenance",
        "notes": [
            "The current goal is weight maintenance and improving nutrition quality."
        ]
    }


