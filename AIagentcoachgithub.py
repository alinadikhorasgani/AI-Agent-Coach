def start_chat():
    state = {
        "step": 0,
        "answers": {}
    }

    history = [
        {
            "role": "assistant",
            "content": (
                "Welcome to the AI Nutrition Coach.\n\n"
                "I will ask a few questions, calculate BMI, BMR, TDEE, estimate a calorie target, "
                "analyze foods with USDA FoodData Central, and generate a personalized nutrition report.\n\n"
                f"{QUESTIONS[0]['question']}"
            )
        }
    ]

    return history, state


def chatbot_reply(user_message, history, state):
    if history is None:
        history = []

    if state is None:
        history, state = start_chat()

    user_message = str(user_message).strip()

    if not user_message:
        return "", history, state

    history.append({
        "role": "user",
        "content": user_message
    })

    step = state["step"]

    if step >= len(QUESTIONS):
        history.append({
            "role": "assistant",
            "content": "The questionnaire is already complete. Click Restart to start again."
        })
        return "", history, state

    current_question = QUESTIONS[step]

    try:
        parsed_answer = current_question["parser"](user_message)
        state["answers"][current_question["key"]] = parsed_answer
        state["step"] += 1

    except Exception as error:
        history.append({
            "role": "assistant",
            "content": (
                f"I could not understand that answer.\n\n"
                f"Error: {str(error)}\n\n"
                f"{current_question['question']}"
            )
        })
        return "", history, state

    if state["step"] < len(QUESTIONS):
        next_question = QUESTIONS[state["step"]]["question"]
        history.append({
            "role": "assistant",
            "content": next_question
        })
        return "", history, state

    history.append({
        "role": "assistant",
        "content": "Thank you. I am generating your nutrition report now."
    })

    try:
        report = generate_report(state["answers"])

    except Exception as error:
        report = (
            "An error occurred while generating the report.\n\n"
            f"Error details: {str(error)}\n\n"
            "For USDA FoodData Central, food names should be entered in English.\n"
            "Example: banana 120g, boiled egg 100g, cooked rice 180g"
        )

    history.append({
        "role": "assistant",
        "content": report
    })

    return "", history, state


