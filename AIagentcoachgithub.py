def parse_number_answer(text, field_name, min_value=None, max_value=None):
    match = re.search(r"\d+(\.\d+)?", str(text))

    if not match:
        raise ValueError(f"Please enter a valid number for {field_name}.")

    value = float(match.group(0))

    if min_value is not None and value < min_value:
        raise ValueError(f"{field_name} is too low.")

    if max_value is not None and value > max_value:
        raise ValueError(f"{field_name} is too high.")

    return value


def parse_gender(text):
    value = str(text).lower().strip()

    if value in ["1", "male", "man", "boy", "m"]:
        return "male"

    if value in ["2", "female", "woman", "girl", "f"]:
        return "female"

    return "unknown"


def parse_activity(text):
    value = str(text).lower().strip()

    number_map = {
        "1": "sedentary",
        "2": "light",
        "3": "moderate",
        "4": "active",
        "5": "very_active"
    }

    digit = re.search(r"[1-5]", value)
    if digit:
        return number_map[digit.group(0)]

    if "sedentary" in value:
        return "sedentary"
    if "light" in value:
        return "light"
    if "moderate" in value:
        return "moderate"
    if "very" in value:
        return "very_active"
    if "active" in value:
        return "active"

    return "sedentary"


def parse_goal(text):
    value = str(text).lower().strip()

    if "1" in value or "maintain" in value:
        return "maintain"

    if "2" in value or "lose" in value or "fat" in value:
        return "lose_weight"

    if "3" in value or "gain" in value or "muscle" in value:
        return "gain_weight"

    return "maintain"


def parse_conditions(text):
    value = str(text).lower().strip()

    if value in ["none", "no", "nothing", "n/a", "na"]:
        return []

    conditions = []

    condition_keywords = {
        "diabetes": ["diabetes", "blood sugar", "glucose"],
        "hypertension": ["hypertension", "blood pressure", "high pressure"],
        "high_cholesterol": ["cholesterol", "triglyceride", "lipid"],
        "kidney_disease": ["kidney", "renal"],
        "liver_disease": ["liver", "hepatic"],
        "anemia": ["anemia", "iron deficiency"]
    }

    for condition, keywords in condition_keywords.items():
        if any(keyword in value for keyword in keywords):
            conditions.append(condition)

    return conditions


