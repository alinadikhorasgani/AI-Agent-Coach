def extract_nutrients_per_100g(food_detail):
    nutrients = {}

    for item in food_detail.get("foodNutrients", []):
        nutrient_obj = item.get("nutrient", {})
        nutrient_number = str(nutrient_obj.get("number", "")).strip()
        nutrient_name = str(nutrient_obj.get("name", "")).lower().strip()
        unit_name = str(nutrient_obj.get("unitName", "")).lower().strip()
        amount = item.get("amount")

        if amount is None:
            continue

        key = NUTRIENT_NUMBER_MAP.get(nutrient_number)

        if not key:
            for name_part, mapped_key in NUTRIENT_NAME_FALLBACK.items():
                if name_part in nutrient_name:
                    key = mapped_key
                    break

        # Avoid treating kJ as kcal.
        if key == "calories_kcal" and unit_name not in ["kcal", "calorie", "calories"]:
            continue

        if key and key not in nutrients:
            nutrients[key] = round(float(amount), 2)

    return nutrients


def scale_nutrients(nutrients_per_100g, grams):
    factor = grams / 100.0

    return {
        nutrient: round(value * factor, 2)
        for nutrient, value in nutrients_per_100g.items()
    }


def add_nutrients(total, item):
    for nutrient, value in item.items():
        total[nutrient] = round(total.get(nutrient, 0) + value, 2)

    return total


