FDC_BASE_URL = "https://api.nal.usda.gov/fdc/v1"


class FoodDataCentralClient:
    def __init__(self, api_key):
        self.api_key = api_key

    def search_food(self, query, page_size=5):
        url = f"{FDC_BASE_URL}/foods/search"

        payload = {
            "query": query,
            "pageSize": page_size,
            "dataType": [
                "Foundation",
                "SR Legacy",
                "Survey (FNDDS)",
                "Branded"
            ]
        }

        response = requests.post(
            url,
            params={"api_key": self.api_key},
            json=payload,
            timeout=30
        )

        response.raise_for_status()

        foods = response.json().get("foods", [])

        priority = {
            "Foundation": 1,
            "SR Legacy": 2,
            "Survey (FNDDS)": 3,
            "Branded": 4
        }

        foods = sorted(
            foods,
            key=lambda item: priority.get(item.get("dataType", ""), 99)
        )

        return foods

    def get_food_details(self, fdc_id):
        url = f"{FDC_BASE_URL}/food/{fdc_id}"

        response = requests.get(
            url,
            params={"api_key": self.api_key},
            timeout=30
        )

        response.raise_for_status()
        return response.json()


