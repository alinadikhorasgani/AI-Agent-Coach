def parse_foods_text(text):
    """
    Expected format:
    banana 120g, boiled egg 100g, cooked rice 180g

    USDA works best with English food names.
    """
    if not text:
        return []

    normalized = text.replace("،", ",")
    chunks = [chunk.strip() for chunk in normalized.split(",") if chunk.strip()]

    foods = []

    for chunk in chunks:
        grams = 100.0

        match = re.search(
            r"(\d+(\.\d+)?)\s*(g|gram|grams)",
            chunk,
            flags=re.IGNORECASE
        )

        if match:
            grams = float(match.group(1))
            name = chunk[:match.start()].strip()
        else:
            name = chunk.strip()

        name = re.sub(r"\s+", " ", name).strip()

        if name:
            foods.append({
                "name": name,
                "grams": grams
            })

    return foods


def analyze_one_food(food):
    food_name = food["name"]
    grams = float(food.get("grams", 100))

    search_results = fdc_client.search_food(food_name, page_size=5)

    if not search_results:
        raise ValueError(f"No USDA match found for: {food_name}")

    selected_food = search_results[0]
    fdc_id = selected_food["fdcId"]

    food_detail = fdc_client.get_food_details(fdc_id)

    nutrients_per_100g = extract_nutrients_per_100g(food_detail)
    scaled_nutrients = scale_nutrients(nutrients_per_100g, grams)

    return {
        "input_name": food_name,
        "matched_name": food_detail.get("description", selected_food.get("description")),
        "fdc_id": fdc_id,
        "data_type": food_detail.get("dataType", selected_food.get("dataType")),
        "grams": grams,
        "nutrients": scaled_nutrients
    }


def analyze_meal(foods):
    items = []
    totals = {}
    not_found = []

    for food in foods:
        try:
            analyzed_item = analyze_one_food(food)
            items.append(analyzed_item)
            totals = add_nutrients(totals, analyzed_item["nutrients"])
        except Exception as error:
            not_found.append({
                "food": food.get("name"),
                "error": str(error)
            })

    return {
        "items": items,
        "totals": totals,
        "not_found": not_found
    }


