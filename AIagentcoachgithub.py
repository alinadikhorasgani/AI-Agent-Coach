def generate_report(answers):
    age = int(answers["age"])
    gender = answers["gender"]
    height_cm = float(answers["height_cm"])
    weight_kg = float(answers["weight_kg"])
    activity_level = answers["activity_level"]
    goal = answers["goal"]

    profile = {
        "age": age,
        "gender": gender,
        "height_cm": height_cm,
        "weight_kg": weight_kg,
        "activity_level": activity_level,
        "goal": goal,
        "conditions": answers.get("conditions", []),
        "allergies_or_restrictions": answers.get("allergies_or_restrictions", "none"),
        "meal_text": answers.get("meal_text", "")
    }

    bmi = calculate_bmi(weight_kg, height_cm)
    bmr = calculate_bmr(weight_kg, height_cm, age, gender)
    tdee = calculate_tdee(bmr, activity_level)

    body_report = {
        "bmi": bmi,
        "bmi_category": bmi_category(bmi),
        "bmr": bmr,
        "tdee": tdee,
        "calorie_plan": calculate_calorie_target(age, bmr, tdee, goal)
    }

    foods = parse_foods_text(profile["meal_text"])
    meal_report = analyze_meal(foods) if foods else None

    recommendations = build_rule_based_recommendations(
        profile=profile,
        body_report=body_report,
        meal_report=meal_report
    )

    llm_answer = generate_llm_response(
        profile=profile,
        body_report=body_report,
        meal_report=meal_report,
        recommendations=recommendations
    )

    if llm_answer:
        return llm_answer

    return fallback_response(
        profile=profile,
        body_report=body_report,
        meal_report=meal_report,
        recommendations=recommendations
    )


